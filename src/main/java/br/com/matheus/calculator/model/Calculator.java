package br.com.matheus.calculator.model;

public class Calculator {
    public static float sum(float n1, float n2) {
        return n1 + n2;
    }

    public static float minus(float n1, float n2) {
        return n1 - n2;
    }

    public static float times(float n1, float n2) {
        return n1 * n2;
    }

    public static float divide(float n1, float n2) {
        return n1 / n2;
    }
}